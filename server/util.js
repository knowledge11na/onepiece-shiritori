const CARD_TYPES = [
  "キャラ（2年前）",
  "キャラ（2年後）",
  "技（2年前）",
  "技（2年後）",
  "5文字",
  "サブタイ（2年前）",
  "サブタイ（2年後）",
  "キャラ（海賊）",
  "キャラ（海賊以外）",
  "10文字以上",
  "技（海賊）",
  "技（海賊以外）",
  "キャラ、技、サブタイ以外"
];

export function createDeck() {
  const deck = [];

  for (let i = 0; i < 100; i++) {
    deck.push({
      id: Math.random().toString(36).slice(2),
      type: CARD_TYPES[Math.floor(Math.random() * CARD_TYPES.length)]
    });
  }

  return shuffle(deck);
}

export function draw(deck, n) {
  return deck.splice(0, n);
}

function shuffle(a) {
  return a.sort(() => Math.random() - 0.5);
}

export function randomKana() {
  const list = ["あ","い","う","え","お","か","き","く","け","こ","さ","し","す","せ","そ","た","ち","つ","て","と","な","に","ぬ","ね","の","は","ひ","ふ","へ","ほ","ま","み","む","め","も","や","ゆ","よ","ら","り","る","れ","ろ","わ"];
  return list[Math.floor(Math.random() * list.length)];
}

export function getLastKana(word) {
  const c = [...word];
  let last = c[c.length - 1];
  if (last === "ー") last = c[c.length - 2];

  // 許容する読みのマップ
  const map = {
    "ゃ":["ゃ","や"],
    "ゅ":["ゅ","ゆ"],
    "ょ":["ょ","よ"],
    "っ":["っ","つ"],
    "ぁ":["ぁ","あ"],
    "ぃ":["ぃ","い"],
    "ぅ":["ぅ","う"],
    "ぇ":["ぇ","え"],
    "ぉ":["ぉ","お"],

    "が":["が","か"],"ぎ":["ぎ","き"],"ぐ":["ぐ","く"],"げ":["げ","け"],"ご":["ご","こ"],
    "ざ":["ざ","さ"],"じ":["じ","し"],"ず":["ず","す"],"ぜ":["ぜ","せ"],"ぞ":["ぞ","そ"],
    "だ":["だ","た"],"ぢ":["ぢ","ち"],"づ":["づ","つ"],"で":["で","て"],"ど":["ど","と"],
    "ば":["ば","は","ぱ"],"び":["び","ひ","ぴ"],"ぶ":["ぶ","ふ","ぷ"],"べ":["べ","へ","ぺ"],"ぼ":["ぼ","ほ","ぽ"],
    "ぱ":["ぱ","は","ば"],"ぴ":["ぴ","ひ","び"],"ぷ":["ぷ","ふ","ぶ"],"ぺ":["ぺ","へ","べ"],"ぽ":["ぽ","ほ","ぼ"]
  };

  return map[last] || [last]; // 複数候補の配列で返す
}