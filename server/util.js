const CARD_TYPES = [
  "キャラ（2年前）",
  "キャラ（2年後）",
  "技（2年前）",
  "技（2年後）",
  "5文字",
  "サブタイ（2年前）",
  "サブタイ（2年後）",
  "キャラ（海賊）",
  "キャラ（海賊以外）",
  "10文字以上",
  "技（海賊）",
  "技（海賊以外）",
  "キャラ、技、サブタイ以外"
];

export function createDeck() {
  const deck = [];

  for (let i = 0; i < 100; i++) {
    deck.push({
      id: Math.random().toString(36).slice(2),
      type: CARD_TYPES[Math.floor(Math.random() * CARD_TYPES.length)]
    });
  }

  return shuffle(deck);
}

export function draw(deck, n) {
  return deck.splice(0, n);
}

function shuffle(a) {
  return a.sort(() => Math.random() - 0.5);
}

// ===============================
// ひらがな変換（カタカナ対応）
// ===============================
export function toHiragana(str) {
  return str.replace(/[ァ-ン]/g, s =>
    String.fromCharCode(s.charCodeAt(0) - 0x60)
  );
}

// ===============================
// ランダム開始文字
// ===============================
export function randomKana() {
  const list = [
    "あ","い","う","え","お",
    "か","き","く","け","こ",
    "さ","し","す","せ","そ",
    "た","ち","つ","て","と",
    "な","に","ぬ","ね","の",
    "は","ひ","ふ","へ","ほ",
    "ま","み","む","め","も",
    "や","ゆ","よ",
    "ら","り","る","れ","ろ",
    "わ"
  ];
  return list[Math.floor(Math.random() * list.length)];
}

// ===============================
// 最後の文字取得（最強版）
// ===============================
export function getLastKana(word) {
  if (!word) return [];

  // ① カタカナ → ひらがな
  word = toHiragana(word);

  const c = [...word];

  // ② 長音対応（ー）
  let last = c[c.length - 1];
  if (last === "ー") {
    last = c[c.length - 2];
  }

  // ===============================
  // 許容マップ
  // ===============================
  const map = {
    // 小文字
    "ゃ":["ゃ","や"],
    "ゅ":["ゅ","ゆ"],
    "ょ":["ょ","よ"],
    "っ":["っ","つ"],
    "ぁ":["ぁ","あ"],
    "ぃ":["ぃ","い"],
    "ぅ":["ぅ","う"],
    "ぇ":["ぇ","え"],
    "ぉ":["ぉ","お"],

    // 濁音（→ 清音もOK）
    "が":["が","か"],"ぎ":["ぎ","き"],"ぐ":["ぐ","く"],"げ":["げ","け"],"ご":["ご","こ"],
    "ざ":["ざ","さ"],"じ":["じ","し"],"ず":["ず","す"],"ぜ":["ぜ","せ"],"ぞ":["ぞ","そ"],
    "だ":["だ","た"],"ぢ":["ぢ","ち"],"づ":["づ","つ"],"で":["で","て"],"ど":["ど","と"],
    "ば":["ば","は","ぱ"],"び":["び","ひ","ぴ"],"ぶ":["ぶ","ふ","ぷ"],"べ":["べ","へ","ぺ"],"ぼ":["ぼ","ほ","ぽ"],

    // 清音（→ 濁音もOK）
    "か":["が","か"],"き":["ぎ","き"],"く":["ぐ","く"],"け":["げ","け"],"こ":["ご","こ"],
    "さ":["ざ","さ"],"し":["じ","し"],"す":["ず","す"],"せ":["ぜ","せ"],"そ":["ぞ","そ"],
    "た":["だ","た"],"ち":["ぢ","ち"],"つ":["づ","つ"],"て":["で","て"],"と":["ど","と"],
    "は":["ば","は","ぱ"],"ひ":["び","ひ","ぴ"],"ふ":["ぶ","ふ","ぷ"],"へ":["べ","へ","ぺ"],"ほ":["ぼ","ほ","ぽ"],

    // 半濁音
    "ぱ":["ぱ","は","ば"],"ぴ":["ぴ","ひ","び"],"ぷ":["ぷ","ふ","ぶ"],"ぺ":["ぺ","へ","べ"],"ぽ":["ぽ","ほ","ぼ"]
  };

  return map[last] || [last];
}